ENV = {**os.environ, "HTTP_PROXY": "", "HTTPS_PROXY": "", "NO_PROXY": "*"}
PYTHON = r".venv\Scripts\python.exe"

print(f"Total specs to check: {len(ALL_SPECS)}")

found = []
not_found = []
errors = []

for i, spec in enumerate(ALL_SPECS):
    try:
        r = subprocess.run(
            [PYTHON, "-m", "_pipeline", "metadata", "fetch", spec],
            capture_output=True, text=True, timeout=25, env=ENV
        )
        out = r.stdout + r.stderr
        if "[FAIL]" in out or "no metadata" in out or "not found" in out.lower():
            not_found.append(spec)
        elif "releases cached" in out or "release cached" in out:
            # Extract first line with spec number and title
            for line in out.splitlines():
                stripped = line.strip()
                if stripped and ":" in stripped and not stripped.startswith("[") and not stripped.startswith("  "):
                    # Check if this line starts with the spec number
                    first_part = stripped.split(":")[0].strip()
                    if spec.replace(" ","") in first_part.replace(" ",""):
                        title = stripped.split(":", 1)[1].strip()
                        found.append((spec, title))
                        break
            else:
                # No matching line, still found
                found.append((spec, ""))
        else:
            # Anything else - assume found
            found.append((spec, ""))
    except subprocess.TimeoutExpired:
        errors.append(f"{spec}: timeout")
    except Exception as e:
        errors.append(f"{spec}: {e}")

    if (i+1) % 100 == 0:
        print(f"Progress: {i+1}/{len(ALL_SPECS)}  found={len(found)}  not_found={len(not_found)}  errors={len(errors)}")

